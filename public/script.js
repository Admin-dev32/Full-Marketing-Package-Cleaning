const languageButtons = document.querySelectorAll('.lang-button');
const translatableElements = document.querySelectorAll('[data-translation-key]');
const revealElements = document.querySelectorAll('.reveal');
const scrollTriggers = document.querySelectorAll('[data-scroll-target]');

let currentLanguage = 'es';

function updateLanguage(lang) {
  const selectedTranslations = translations[lang];
  if (!selectedTranslations) return;

  translatableElements.forEach((element) => {
    const key = element.getAttribute('data-translation-key');
    if (selectedTranslations[key]) {
      element.textContent = selectedTranslations[key];
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle('active', button.dataset.lang === lang);
  });

  document.documentElement.lang = lang;
  currentLanguage = lang;
}

languageButtons.forEach((button) => {
  button.addEventListener('click', () => {
    const lang = button.dataset.lang;
    if (lang && lang !== currentLanguage) {
      updateLanguage(lang);
    }
  });
});

updateLanguage(currentLanguage);

const prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

function createRevealObserver() {
  if (prefersReducedMotion) {
    revealElements.forEach((element) => element.classList.add('reveal--visible'));
    return null;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('reveal--visible');
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.18,
      rootMargin: '0px 0px -10% 0px'
    }
  );

  revealElements.forEach((element) => observer.observe(element));
  return observer;
}

createRevealObserver();

scrollTriggers.forEach((trigger) => {
  trigger.addEventListener('click', (event) => {
    const targetSelector = trigger.getAttribute('data-scroll-target');
    const targetElement = targetSelector ? document.querySelector(targetSelector) : null;

    if (targetElement) {
      event.preventDefault();
      targetElement.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
  });
});
